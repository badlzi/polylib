    public String getGiaByTenS(String ten) {
        SL = SD.selectAll();
        String gia = "";
        for (Sach s : SL) {
            if (s.getMaSach().equals(ten)) {
                gia = s.getTenSach();
            }
        }
        return gia;
    }

    public String getGiaByMaSach(String ten) {
        SL = SD.selectAll();
        String gia = "";
        for (Sach s : SL) {
            if (s.getTenSach().equals(ten)) {
                gia = s.getMaSach();
            }
        }
        return gia;
    }

