     void insert() {
        PhieuTra model = getForm();
        try {
            PTD.insert(model);
            this.HienThiLenban();
            this.clear();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }

    void clear() {
        txtSoPhieuTra.setText("");
        txtSoPhieuMuon.setText("");
        txtNgayTra.setText("");
        txtTinhTrang.setText("");
        txtTienPhat.setText("");
        
    }

    void update() {
        PhieuTra model = getForm();
        try {
            PTD.update(model);
            this.HienThiLenban();
            MsgBox.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại!");
        }
    }

    void delete() {
        if (MsgBox.confirm(this, "Bạn thực sự muốn xóa người học này?")) {
            String maNXB = txtSoPhieuTra.getText();
            try {
                PTD.delete(maNXB);
                this.HienThiLenban();
                this.clear();
                MsgBox.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Xóa thất bại!");
            }
        }
    }
    void first() {
        this.row = 0;
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }else{
            this.row = tbl_PhieuTra.getRowCount() - 1;
            this.edit();
        }
    }

    void next() {
        if (this.row <= tbl_PhieuTra.getRowCount() - 1) {
            this.row++;
            this.edit();
        }else{
            this.row = 0;
            this.edit();
        }
    }

    void last() {
        this.row = tbl_PhieuTra.getRowCount() - 1;
        this.edit();
    }
