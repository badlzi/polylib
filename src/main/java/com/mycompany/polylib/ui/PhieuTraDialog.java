    void setForm(PhieuTra pt) {
        txtSoPhieuTra.setText(pt.getSoPhieuTra());
        txtSoPhieuMuon.setText(pt.getPhieuMuon().getSoPhieuMuon());
        txtNgayTra.setText(XDate.toString(pt.getNgayTra(), "MM/dd/yyyy"));
        txtTinhTrang.setText(pt.getTinhTrangSach());
        txtTienPhat.setText(String.valueOf(pt.getTienPhat()));
    }

    PhieuTra getForm() {
        PhieuTra pt = new PhieuTra();
        pt.setSoPhieuTra(txtSoPhieuTra.getText());
        PhieuMuon pm = new PhieuMuon();
        pm.setSoPhieuMuon(txtSoPhieuMuon.getText());
        pt.setPhieuMuon(pm);
        pt.setNgayTra(XDate.toDate("MM/dd/yyyy", txtNgayTra.getText()));
        pt.setTinhTrangSach(txtTinhTrang.getText());
        pt.setTienPhat(Double.parseDouble(txtTienPhat.getText()));
        return pt;
    }
